class Cybernetikz_Cnslider_Block_Adminhtml_Cnslider_Edit_Form_Element_Image extends Varien_Data_Form_Element_Image
{
    /**
     * Get image preview url
     *
     * @return string
     */
    protected function _getUrl()
    {
        $url = false;
        if ($this->getValue()) {
            $url = Mage::helper('cybernetikz_cnslider/image')->getBaseUrl() . '/' . $this->getValue();
        }
        return $url;
    }
}